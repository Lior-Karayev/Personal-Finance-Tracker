package main;

/**
 * Represents categories for financial transactions.
 */
public enum Category {
    /** Expenses related to food, groceries, or dining. */
    FOOD,

    /** Transportation-related costs like fuel, taxi, or public transport. */
    TRANSPORT,

    /** Utility bills such as electricity, water, or internet. */
    UTILITIES,

    /** Salaries or wages received. */
    SALARY,

    /** Entertainment expenses like movies, subscriptions, or games. */
    ENTERTAINMENT,

    /** Medical or health-related expenses. */
    HEALTH,

    /** Miscellaneous or uncategorized expenses. */
    OTHER
}
